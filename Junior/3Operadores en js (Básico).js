// Operadores en js (Básico)

// Asignación

/*
    Los operadores de asignación asigna un valor al operando de la izquierda
    basado en el valor del operando de la derecha.
*/

x = y; // Asignación
x += y; // Asignación de adicción
x -= y; // Asignación de sustracción
x *= y; // Asignación de multiplicación
x /= y; // Asignación de división
x %= y; // Asignación de Resto
x **= y; // Asignación de exponenciación
x <<= y; // Asignación de desplazamiento a la izquierda
x >>= y; // Asignación de desplazamiento a la derecha
x >>>= y; // AAsignación sin signo de desplazamiento a la derecha
x &= y; // Asignación AND
x ^= y; // Asignación XOR
x |= y; // Asignación OR

